package com.george.formaven;

public class MathOps {
	public static int add(int i, int j) {
		return i+j;
	}
	public static int subtract(int i, int j) {
		return i - j;
	}
	public static int multiply(int i, int j) {
		return i - j;
	}
	public static int divide(int i, int j) {
		return i / j;
	}
}
